#include<bits/stdc++.h>
using namespace std;

int main(){
    int res = 2;

    switch (res)
    {
        case 0:
            cout<<"Zero"<<endl;
            break;
        case 1:
            cout<<"First"<<endl;
            break;
        case 2:
            cout<<"Second"<<endl;
            break;
        case 3:
            cout<<"Third"<<endl;
            break;
        case 4:
            cout<<"Fourth"<<endl;
    }
    return 0;
}